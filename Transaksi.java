    private void CicilanCbxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CicilanCbxActionPerformed
       long Harga = Long.parseLong(HargaTxt.getText()), totalbunga, ppn;
        switch (CicilanCbx.getSelectedIndex()){
            case 0:
                BungaTxt.setText("");
                PpnTxt.setText("");
                break;
            case 1:
                totalbunga= Harga * 7/100;
                ppn = Harga * 10/100;
                BungaTxt.setText(""+totalbunga);
                PpnTxt.setText(""+ppn);
                break;
            case 2:
                totalbunga = Harga * 8/100;
                ppn = Harga * 10/100;
                BungaTxt.setText(""+totalbunga);
                PpnTxt.setText(""+ppn);
                break;
            default:
                totalbunga = Harga * 9/100;
                ppn = Harga * 10/100;
                BungaTxt.setText(""+totalbunga);
                PpnTxt.setText(""+ppn);
                break;
        
        }
    }//GEN-LAST:event_CicilanCbxActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(NamaTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Belum Di Isi !!");
        }else if (KtpTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "KTP Belum Di Isi !!");
        }else if (HpTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No HP Belum DI Isi !!");
        }else if(HargaTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silakan isi data mobil yang ingin dibeli !");
        }else if (DpTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Isi DP dahulu !");
        }else if(PpnTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Pilih Lama Cicilan !");
        }else{
            long harga = Long.parseLong(HargaTxt.getText()),
                    
                    ppn = Long.parseLong(PpnTxt.getText()),
                    bunga = Long.parseLong(BungaTxt.getText()),
                    total;
            total = harga+ppn+bunga;
            Totaltxt.setText(""+total);
            
            if (CicilanCbx.getSelectedItem()== "24"){
                int cicilan = 24;
                long lamacicilan = Long.parseLong(Totaltxt.getText()), 
                        dp = Long.parseLong(DpTxt.getText()),
                        totalcicilan,price, tobay;
                price = lamacicilan - dp;
                totalcicilan = price / cicilan;
                tobay = totalcicilan + dp;
                TotalCicilanTxt.setText(""+totalcicilan);
                TobayTxt.setText(kursIndonesia.format(tobay));
                
            }else if (CicilanCbx.getSelectedItem() == "36"){
                int cicilan = 36;
                long lamacicilan = Long.parseLong(Totaltxt.getText()), 
                        dp = Long.parseLong(DpTxt.getText()),
                        totalcicilan, price,tobay;
                price = lamacicilan - dp;
                totalcicilan = price / cicilan;
                tobay = totalcicilan + dp;
                TotalCicilanTxt.setText(""+totalcicilan);
                TobayTxt.setText(kursIndonesia.format(tobay));
            }else if(CicilanCbx.getSelectedItem() == "48"){
                int cicilan = 48;
                long lamacicilan = Long.parseLong(Totaltxt.getText()),
                        dp = Long.parseLong(DpTxt.getText()),price,totalcicilan, tobay;
                price = lamacicilan - dp;
                totalcicilan = price / cicilan;
                tobay = totalcicilan + dp;
                TotalCicilanTxt.setText(""+totalcicilan);
               TobayTxt.setText(kursIndonesia.format(tobay));
            }else{
                JOptionPane.showMessageDialog(null, "Lama Cicilan Belum Di Pilih !!");
            }
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

