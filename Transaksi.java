private void autonumber(){
       try{
           Connection conn = koneksi.configDB();
           Statement stat = conn.createStatement();
           ResultSet rs = stat.executeQuery("SELECT * FROM data order by id desc");
           if (rs.next()){
               String kode = rs.getString("id").substring(4);
               String AN = "" +(Integer.parseInt(kode) + 1);
               String nol = "";
               
               if(AN.length()==1)
               {nol = "000";}
               else if(AN.length()==2)
               {nol = "00";}
               else if(AN.length()==3)
               {nol = "0";}
               else if(AN.length()==4)
               {nol = "";}
               
               
               KodeTxt.setText("PLG" + nol + AN);
                                 
           }else{
               KodeTxt.setText("PLG0001");
           }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
    }


