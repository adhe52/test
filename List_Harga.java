private void data_mobil(){
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No");
    model.addColumn("ID");
    model.addColumn("Merk");
    model.addColumn("Type");
    model.addColumn("Harga");
    model.addColumn("Tahun Pembuatan");
    
    String cari = CariTxt.getText();
    
    try{
        int no= 1;
        String sql = "select * from mobil WHERE Merk LIKE '%"+cari+"%'";
        java.sql.Connection conn = (java.sql.Connection)(Connection)koneksi.configDB();
        java.sql.Statement sm = conn.createStatement();
        java.sql.ResultSet rs = sm.executeQuery(sql);
        while (rs.next()){
            model.addRow(new Object[]{no++, rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
        
        }
        TabelMobil.setModel(model);
        
    }catch(Exception e){
    
    }
    

}


    
