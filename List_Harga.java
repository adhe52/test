    private void TabelMobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelMobilMouseClicked
        int baris = TabelMobil.rowAtPoint(evt.getPoint());
       
       String id = TabelMobil.getValueAt(baris , 1).toString();
       IdTxt.setText(id);
       
       String merk = TabelMobil.getValueAt(baris, 2).toString();
       MerkTxt.setText(merk);
       
       String type = TabelMobil.getValueAt(baris, 3).toString();
       TypeTxt.setText(type);
       
       String harga = TabelMobil.getValueAt(baris, 4).toString();
       HargaTxt.setText(harga);
       
       String tahun = TabelMobil.getValueAt(baris, 5).toString();
       TahunTxt.setText(tahun);
    }//GEN-LAST:event_TabelMobilMouseClicked

